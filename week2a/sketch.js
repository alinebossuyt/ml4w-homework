let video;
let poseNet;
let poses = [];
let lEye;
let rEye;

function setup() {
  createCanvas(640, 480);
  video = createCapture(VIDEO);
  video.size(width, height);

  // Create a new poseNet method with a single detection
  poseNet = ml5.poseNet(video, modelReady);
  // This sets up an event that fills the global variable "poses"
  // with an array every time new poses are detected
  poseNet.on('pose', function(results) {
    poses = results;
  });
  // Hide the video element, and just show the canvas
  video.hide();
}

function modelReady() {
  select('#status').html('Model Loaded');
}

function draw() {
  image(video, 0, 0, width, height);

  // We can call both functions to draw all keypoints and the skeletons
  drawKeypoints();
  //drawSkeleton();
}

// A function to draw ellipses over the detected keypoints
function drawKeypoints()  {
  // Loop through all the poses detected
  for (let i = 0; i < poses.length; i++) {
    // For each pose detected, loop through all the keypoints
    let pose = poses[i].pose;
    rEye = pose['rightEye'];
    lEye = pose['leftEye'];
    drawCensorBar();

  }
}

// A function to draw the skeletons
function drawSkeleton() {
  // Loop through all the skeletons detected
  for (let i = 0; i < poses.length; i++) {
    let skeleton = poses[i].skeleton;
    // For every skeleton, loop through all body connections
    for (let j = 0; j < skeleton.length; j++) {
      let partA = skeleton[j][0];
      let partB = skeleton[j][1];
      stroke(255, 0, 0);
      line(partA.position.x, partA.position.y, partB.position.x, partB.position.y);
    }
  }
}

function drawCensorBar(){
  console.log("right eye"+rEye);
  let scale = 3;
  let eye_w = dist(rEye.x,rEye.y, lEye.x,lEye.y) * scale ;
  let eye_h = eye_w/4;
  let center_x = rEye.x + ((lEye.x-rEye.x)/2);
  let center_y = rEye.y +(lEye.y-rEye.y)/2;
  rectMode(CENTER);
  fill(0,0,0);
  noStroke();
  rect(center_x, center_y, eye_w, eye_h);

}
